def kb_channel_link() -> InlineKeyboardMarkup:
    """Клавиатура с кнопкой-ссылкой на ТГ-канал."""
    return InlineKeyboardMarkup(inline_keyboard=[
        [InlineKeyboardButton(
            text="📢 Подписаться на канал",
            url=config.TG_CHANNEL_LINK,
        )]
    ])


